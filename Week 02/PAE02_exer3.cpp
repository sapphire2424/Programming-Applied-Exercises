/* 2025年度 プログラミング応用演習 第2回 演習プログラム(3) - PAE02_exer3
====================================================================================================
■キー入力された整数(0～255)を2進数で表示するプログラム
・範囲外の値が入力された場合はエラーを表示して終了する．
==================================================================================================*/
#include <iostream>
using namespace std;
int main(void)
{
    /* 変数定義 */
    unsigned int   dec_input;       /* 整数入力 */
    int     i;

    /* 0～255の整数を入力する */
    cout << ">>> 0～255の整数:";
    cin >> dec_input;
    /* 入力範囲のチェック */
    if (dec_input > 255 || dec_input < 0) {
        cout << "Error: 0～255の範囲外";
        return 1;
    }

    /* 10進数→2進数変換ループ・表示 */
    printf("%dの2進数表記は", dec_input);
    for (i = 7; i >= 0; i--) {
        cout << ((dec_input >> i) & 1);
    }
    return 0;
}


